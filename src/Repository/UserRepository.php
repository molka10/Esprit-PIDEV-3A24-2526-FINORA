    /**
     * Finds the first user with ROLE_ADMIN to receive module notifications.
     */
    public function findOneAdmin(): ?User
    {
        return $this->createQueryBuilder('u')
            ->andWhere('UPPER(u.role) = :role')
            ->setParameter('role', 'ADMIN')
            ->setMaxResults(1)
            ->getQuery()
            ->getOneOrNullResult();
    }
